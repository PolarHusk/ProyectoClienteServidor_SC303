    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String usuario = txtUsuario.getText();
        String contrasena = String.valueOf(txtContrasena.getPassword());

        InicioSesion controlador = new InicioSesion();

        Usuario logueado = controlador.iniciarSesion(usuario, contrasena);

        if (logueado == null) {
            JOptionPane.showMessageDialog(this, "Usuario o contrasena invalidos");
            return;
        }

        switch (logueado.getRolUsuario()) {
            case ADMIN:
                VistaAdmin vistaAdmin = new VistaAdmin(logueado);
                new ControladorAdmin(vistaAdmin);
                vistaAdmin.setVisible(true);
                break;
            case TECNICO:
                VistaTecnico vistaTecnico = new VistaTecnico(logueado);
                new ControladorTecnico(vistaTecnico);
                vistaTecnico.setVisible(true);
                break;
            case ENDUSER:
                VistaEndUser vistaEndUser = new VistaEndUser(logueado);
                new ControladorEndUser(vistaEndUser);
                vistaEndUser.setVisible(true);
                break;
        }

        this.dispose();

    }//GEN-LAST:event_btnLoginActionPerformed

